<h3>Método de Pago</h3>

<br>
<p>Puedes pagar con efectivo o tarjetas debido. Todas las transacciones son ejecutadas por nuestra
pasarela de pago aliada Wompi, la cual cuenta con las certificaciones de seguridad y privacidad
exigidas por los estándares internaciones y por la normativa del país. Los datos de las transacciones y
tarjetas estarán encriptados en la pasarela de pago, por lo cual no será posible que la app o sus
funcionarios accedan a ellos</p>

