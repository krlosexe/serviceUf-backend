<h3>Sobre los servicios</h3>

<p>Los servicios de belleza podrán ser realizados en tu casa, hotel u oficina, o en donde desees. Nuestras
aliadas/os llevarán las herramientas necesarias para realizar el servicio. Sin embargo, para efectos de
higiene y asepsia, será necesario que cuentes con tu propia toalla y con tus propios recipientes para
manos y pies. Somos una organización con conciencia ambiental y por eso buscamos minimizar el
consumo de bolsas plásticas en nuestra operación, por esto mismo no promovemos el uso de
recipientes propios, porque esto implicaría un uso masivo de bolsas desechables.</p>


<p>También recomendamos que tengas tu propio kit de herramientas, en caso de no tenerlo nuestras
aliadas usarán las propias, las cuales se encuentran esterilizadas y desinfectadas.</p>
